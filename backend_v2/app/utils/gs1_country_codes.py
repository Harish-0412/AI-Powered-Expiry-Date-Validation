def get_country_from_prefix(prefix: str) -> str:
    """Return country name based on GS1 country prefix."""
    if not prefix:
        return "Unknown"
    p = int(prefix) if prefix.isdigit() else 0
    if 0 <= p <= 19: return "United States / Canada"
    if 30 <= p <= 39: return "United States"
    if 60 <= p <= 139: return "United States"
    if 300 <= p <= 379: return "France"
    if 400 <= p <= 440: return "Germany"
    if 450 <= p <= 459: return "Japan"
    if 460 <= p <= 469: return "Russia"
    if 471: return "Taiwan"
    if 489: return "Hong Kong"
    if 490 <= p <= 499: return "Japan"
    if 500 <= p <= 509: return "United Kingdom"
    if 540 <= p <= 549: return "Belgium / Luxembourg"
    if 570 <= p <= 579: return "Denmark"
    if 590: return "Poland"
    if 600 <= p <= 601: return "South Africa"
    if 690 <= p <= 699: return "China"
    if 700 <= p <= 709: return "Norway"
    if 730 <= p <= 739: return "Sweden"
    if 760 <= p <= 769: return "Switzerland"
    if 800 <= p <= 839: return "Italy"
    if 840 <= p <= 849: return "Spain"
    if 870 <= p <= 879: return "Netherlands"
    if 880: return "South Korea"
    if 890: return "India"
    if 900 <= p <= 919: return "Austria"
    if 930 <= p <= 939: return "Australia"
    return "Other / Global"
